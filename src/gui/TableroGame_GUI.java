    private void jtbStartGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbStartGameActionPerformed
        if (tableroMatriz1.isGameEnded()){ 
            jtbStartGame.setText("Juego ha terminado");
            return;
        }
        if (jtbStartGame.isSelected()){
            if (!tableroMatriz1.gamePlaying()){
                jtbStartGame.setText("Pausar Juego");
                requestFocus();
                tableroMatriz1.game();
            } else {
                System.out.println("inconsistencia en isSelected=true;");
            }
        } else {
            if (tableroMatriz1.gamePlaying()){
                jtbStartGame.setText("Continuar Juego");
                tableroMatriz1.gamePause();
            } else {
                System.out.println("inconsistencia en isSelected=false;");
            }
        }
    }//GEN-LAST:event_jtbStartGameActionPerformed

