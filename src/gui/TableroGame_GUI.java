    public void displayHeroe(){
        Heroe h = tableroMatriz1.getHeroe();
        jtfHXImagen.setText(h.getX_imagen());
        jlHXImagen.setIcon(new ImageIcon(tableroMatriz1.imageCache.getSprite(h.getX_imagen())));
        jtfHXNombre.setText(h.getX_nombre());
        jtfHXDescripcion.setText(h.getX_descripcion());
        jtfHXDestruir.setText(String.valueOf(h.getX_destruir()));
        setXVida(h.getX_vida());
        displayArma(h.getWeapon());
    }
    public void displayArma(Arma a){
        if (a == null) return;
        jtfAXImagen.setText(a.getX_imagen());
        jlAXImagen.setIcon(new ImageIcon(tableroMatriz1.imageCache.getSprite(a.getX_imagen())));
        jtfAXNombre.setText(a.getX_nombre());
        jtfAXDescripcion.setText(a.getX_descripcion());
        jtfAXDestruir.setText(String.valueOf(a.getX_destruir()));
        jtfAXAlcance.setText(String.valueOf(a.getX_alcance()));
        setXUso(a.getX_uso());
    }
